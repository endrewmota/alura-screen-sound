void ExibirOpcoesDoMenu()
{
    ExibirLogo();
    Console.WriteLine("\nDigite 1 para registrar uma banda");
    Console.WriteLine("Digite 2 para mostrar todas as bandas");
    Console.WriteLine("Digite 3 para avaliar uma banda");
    Console.WriteLine("Digite 4 para exibir a média de uma banda");
    Console.WriteLine("Digite -1 para sair");

    Console.Write("\nDigite a sua opção:");
    string opcaoEscolhida = Console.ReadLine()!;
    int opcaoEscolhidaNumerica = int.Parse(opcaoEscolhida);

    switch (opcaoEscolhidaNumerica)
    {
        case 1: RegistrarBanda();
            break;

        case 2:
            Console.WriteLine("\nVocê escolheu a opção 2");
            break;

        case 3:
            Console.WriteLine("\nVocê escolheu a opção 3");
            break;

        case 4:
            Console.WriteLine("\nVocê escolheu a opção 4");
            break;

        case -1:
            Console.WriteLine("\nVocê escolheu a opção -1");
            break;
    }
}

void RegistrarBanda()
{
    Console.Clear();
    Console.Write("Digite o nome da banda que deseja registrar: ");
    string nomeDaBanda = Console.ReadLine()!;
    Console.WriteLine($"Banda {nomeDaBanda} registrada com sucesso!");
    Thread.Sleep(4000);
    Console.Clear();
    ExibirOpcoesDoMenu();
}

